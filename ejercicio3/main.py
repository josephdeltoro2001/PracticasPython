#Ejemplo con sqrt
from math import sqrt
numero = 30
raiz_cuadrada = sqrt(numero)

print("Raíz cuadrada con sqrt")
print(f"La raíz cuadrada de {numero} es: {raiz_cuadrada}\n")

#Ejemplo con un alias
import math as m
numero = 40
raiz_cuadrada2 = m.sqrt(numero)

print("Raíz cuadrada con un alias")
print(f"La raíz cuadrada de {numero} es: {raiz_cuadrada2}")
