# operaciones_matematicas.py

def suma(numero1, numero2):
    return numero1 + numero2

def resta(numero1, numero2):
    return numero1 - numero2

def multiplicacion(numero1, numero2):
    return numero1 * numero2

def division(numero1, numero2):
    if numero2 != 0:
        return numero1 / numero2
    else:
        return "No es posible dividir por cero."
